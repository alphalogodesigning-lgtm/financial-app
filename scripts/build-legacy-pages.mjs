import fs from 'node:fs/promises';
import path from 'node:path';
import ts from 'typescript';

const roots = ['.', 'public'];

async function fileExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function compileFile(filePath) {
  const source = await fs.readFile(filePath, 'utf8');
  const result = ts.transpileModule(source, {
    compilerOptions: {
      target: ts.ScriptTarget.ES2020,
      module: ts.ModuleKind.None,
      jsx: ts.JsxEmit.React,
      jsxFactory: 'React.createElement',
      jsxFragmentFactory: 'React.Fragment'
    },
    fileName: filePath.endsWith('.js') ? filePath.replace(/\.js$/, '.jsx') : filePath,
    reportDiagnostics: true
  });

  const outPath = filePath.replace(/-app\.js$/, '-app.bundle.js');
  const banner = '// Generated by scripts/build-legacy-pages.mjs. Do not edit directly.\n';
  await fs.writeFile(outPath, `${banner}${result.outputText}`);

  if (result.diagnostics?.length) {
    const errors = result.diagnostics
      .map((diag) => ts.flattenDiagnosticMessageText(diag.messageText, '\n'))
      .join('\n');
    console.warn(`Transpile diagnostics for ${filePath}:\n${errors}`);
  }

  return outPath;
}

async function run() {
  const compiled = [];

  for (const root of roots) {
    const dirEntries = await fs.readdir(root, { withFileTypes: true });
    for (const entry of dirEntries) {
      if (!entry.isFile()) continue;
      if (!entry.name.endsWith('-app.js')) continue;
      const fullPath = path.join(root, entry.name);
      if (!(await fileExists(fullPath))) continue;
      compiled.push(await compileFile(fullPath));
    }
  }

  console.log(`Compiled ${compiled.length} legacy app bundles.`);
  for (const file of compiled) {
    console.log(` - ${file}`);
  }
}

run().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
